    public function callback($request, Transaction $transaction, ThirdChannelModel $thirdChannel)
    {
        $data = $request->all();
        $errorBody = [
            "code" => 100,
            "msg" => "失败"
        ];
        $successBody = [
            "code" => 200,
            "msg" => "成功"
        ];
        $sign = $this->makesign([
            "merchant_id" => $data["merchant_id"],
            "merchant_order_id" => $data["merchant_order_id"],
            "typay_order_id" => $data["typay_order_id"],
            "pay_type" => $data["pay_type"],
            "pay_amt" => number_format($data["pay_amt"], 2, ".", ""),
            "pay_message" => $data["pay_message"],
            "remark" => $data["remark"],
        ], $thirdChannel->key);

        if ($data["sign"] != $sign) {
            return ["error" => "簽名錯誤"];
        }

        if (isset($data['pay_amt']) && $data['pay_amt'] != $transaction->amount) {
            return ['error' => '金额不正确'];
        }
        if ($data['merchant_order_id'] != $transaction->order_number) {
            return ['error' => '订单编号不正确'];
        }

        if (isset($data['pay_message']) && in_array($data['pay_message'], [1])) {
            return ['success' => true, "resBody" => $successBody];
        } else if (isset($data['pay_message']) && in_array($data['pay_message'], ['-2'])) {
            return ['fail' => $data["pay_result"], "resBody" => $successBody, "statusCode" => 200];
        }
        return ['error' => '未知错误'];
    }

    public function queryBalance($data)
    {
        $this->key = $data["key"];
        $postBody = [
            "MerchantID" => intval($data["merchant"]),
            "MerchantType" => 0,
        ];
        $postBody["sign"] = $this->makesign([
            "merchant_id" => $data["merchant"]
        ], $this->key);

        try {
            $client = new \GuzzleHttp\Client();
            $response = $client->request('POST', $data['queryBalanceUrl'], [
                'form_params' => $postBody
            ]);
        } catch (\Exception $e) {
            $message = $e->getMessage();
            Log::error(self::class, compact('data', 'postBody', 'message'));
            return ['success' => true];
        }

        $row = json_decode($response->getBody(), true);
        $balance = $row["balance"];
        ThirdChannelModel::where("id", $data["thirdchannelId"])->update([
            "balance" => $balance,
        ]);
        return $balance;
    }

    public function makesign($body, $key)
    {
        unset($body["sign"]);
        $signStr = urldecode(http_build_query($body)) . "&key=$key";
        return strtolower(md5($signStr));
    }
